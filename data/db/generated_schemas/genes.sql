CREATE TABLE IF NOT EXISTS genes (
	name String,
	source LowCardinality(String),
	source_url LowCardinality(String),
	gene_id String,
	gene_type LowCardinality(String),
	chr LowCardinality(String),
	start UInt32,
	end UInt32,
	strand LowCardinality(String),
	symbol String,
	version LowCardinality(String),
	organism LowCardinality(String),
	study_sets Array(String),
	collections Array(String),
	version_number LowCardinality(String),
	hgnc String,
	synonyms Array(String),
	entrez String,
	id String PRIMARY KEY
);
