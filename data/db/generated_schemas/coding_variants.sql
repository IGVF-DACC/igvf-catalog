CREATE TABLE IF NOT EXISTS coding_variants (
	name String,
	source LowCardinality(String),
	source_url LowCardinality(String),
	ref String,
	alt String,
	aapos UInt16,
	gene_name String,
	protein_name String,
	protein_id String,
	hgvsp String,
	hgvsc String,
	refcodon LowCardinality(String),
	codonpos UInt8,
	transcript_id String,
	SIFT_score Float64 DEFAULT -inf,
	SIFT4G_score Float64 DEFAULT -inf,
	Polyphen2_HDIV_score Float64 DEFAULT -inf,
	Polyphen2_HVAR_score Float64 DEFAULT -inf,
	VEST4_score Float64 DEFAULT -inf,
	REVEL_score Float64 DEFAULT -inf,
	MutPred_score Float64 DEFAULT -inf,
	BayesDel_addAF_score Float64 DEFAULT -inf,
	BayesDel_noAF_score Float64 DEFAULT -inf,
	VARITY_R_score Float64 DEFAULT -inf,
	VARITY_ER_score Float64 DEFAULT -inf,
	VARITY_R_LOO_score Float64 DEFAULT -inf,
	VARITY_ER_LOO_score Float64 DEFAULT -inf,
	ESM1b_score Float64 DEFAULT -inf,
	AlphaMissense_score Float64 DEFAULT -inf,
	CADD_raw_score Float64 DEFAULT -inf,
	id String PRIMARY KEY
);
