CREATE TABLE IF NOT EXISTS coding_variants (
	name String,
	source String,
	source_url String,
	ref String,
	alt String,
	aapos Int64,
	gene_name String,
	protein_name String,
	protein_id String,
	hgvsp String,
	hgvsc String,
	refcodon String,
	codonpos Int64,
	transcript_id String,
	SIFT_score Nullable(Float64),
	SIFT4G_score Nullable(Float64),
	Polyphen2_HDIV_score Nullable(Float64),
	Polyphen2_HVAR_score Nullable(Float64),
	VEST4_score Nullable(Float64),
	REVEL_score Nullable(Float64),
	MutPred_score Nullable(Float64),
	BayesDel_addAF_score Nullable(Float64),
	BayesDel_noAF_score Nullable(Float64),
	VARITY_R_score Nullable(Float64),
	VARITY_ER_score Nullable(Float64),
	VARITY_R_LOO_score Nullable(Float64),
	VARITY_ER_LOO_score Nullable(Float64),
	ESM1b_score Nullable(Float64),
	AlphaMissense_score Nullable(Float64),
	CADD_raw_score Nullable(Float64),
	id String PRIMARY KEY
);
