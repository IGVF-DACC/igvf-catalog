	lead_chrom String,
	lead_pos Float64,
	lead_ref String,
	lead_alt String,
	phenotype_term String,
	direction String,
	beta Float64,
	beta_ci_lower Float64,
	beta_ci_upper Float64,
	odds_ratio Float64,
	oddsr_ci_lower Float64,
	oddsr_ci_upper Float64,
	p_val_mantissa Float64,
	p_val_exponent Float64,
	p_val Float64,
	log10pvalue Float64,
	tagged_variants Array(String),
	source String,
	version String,
	name String,
	inverse_name String,
	id String PRIMARY KEY,
	variants_phenotypes_id String,
	studies_id String
);
